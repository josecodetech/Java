package main;

public class array {

	public static void main(String[] args) {
		// Declaracion
		int[] numeros;
		// Inicializacion
		numeros = new int[5];
		// Asignacion de valores
		numeros[0] = 10;
		numeros[1] = 20;
		numeros[2] = 30;
		numeros[3] = 40;
		numeros[4] = 50;
		// Mostramos valor
		System.out.println("Elemento en posicion 2: "+numeros[2]);
	}

}
